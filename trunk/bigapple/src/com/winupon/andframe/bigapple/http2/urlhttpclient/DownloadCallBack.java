package com.winupon.andframe.bigapple.http2.urlhttpclient;

/**
 * 请求回调
 * 
 * @author xuan
 * @version $Revision: 1.0 $, $Date: 2013-8-7 下午2:12:28 $
 */
public interface DownloadCallBack {

    /**
     * 开始下载
     * 
     * @param count
     */
    public void onStart(long count);

    /**
     * 下载中
     * 
     * @param count
     * @param current
     */
    public void onLoading(long count, long current);

    /**
     * 下载完成
     */
    public void onEnd();
}
